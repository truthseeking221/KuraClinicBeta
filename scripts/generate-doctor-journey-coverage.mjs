import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const workspaceRoot = path.resolve(scriptDirectory, '..');
const journeyDirectory = path.join(
  workspaceRoot,
  'docs/kura-clinic/01-journeys',
);
const catalogPath = path.join(journeyDirectory, 'journey-catalog.md');
const caseMatrixPath = path.join(journeyDirectory, 'journey-case-matrix.md');
const careLoopPath = path.join(journeyDirectory, 'doctor-care-loop-map.md');
const outputPath = path.join(
  journeyDirectory,
  'doctor-journey-coverage-matrix.md',
);

function parseTableRow(line) {
  return line
    .split('|')
    .slice(1, -1)
    .map((cell) => cell.trim());
}

function parseCatalog(markdown) {
  const sections = [];
  let currentSection;

  for (const line of markdown.split('\n')) {
    if (line.startsWith('## ')) {
      currentSection = {
        title: line.slice(3).trim(),
        journeys: [],
      };
      sections.push(currentSection);
      continue;
    }

    if (!/^\| [A-Z]+-\d+ \|/.test(line)) continue;

    const [id, journey, primaryActor, outcome, coverage] =
      parseTableRow(line);

    if (!currentSection) {
      throw new Error(`Journey ${id} appears before a section heading.`);
    }

    currentSection.journeys.push({
      id,
      journey,
      primaryActor,
      outcome,
      coverage,
      section: currentSection.title,
    });
  }

  return sections.filter((section) => section.journeys.length > 0);
}

function parseCaseCoverage(markdown) {
  const casesByJourney = new Map();

  for (const line of markdown.split('\n')) {
    if (!/^\| CASE-[A-Z]+-\d+ \| [A-Z]+-\d+ \|/.test(line)) continue;

    const [caseId, journeyId, tier] = parseTableRow(line);
    const cases = casesByJourney.get(journeyId) ?? [];
    cases.push({ caseId, tier });
    casesByJourney.set(journeyId, cases);
  }

  return casesByJourney;
}

function doctorRelationship({ primaryActor }) {
  if (/doctor/i.test(primaryActor)) return 'Direct doctor action';
  if (/any staff|multi-workspace staff/i.test(primaryActor)) {
    return 'Doctor continuity';
  }
  if (/owner|admin/i.test(primaryActor)) return 'Administration boundary';
  if (/authorized staff/i.test(primaryActor)) {
    return 'Authorization boundary';
  }
  if (/system/i.test(primaryActor)) return 'System dependency';
  if (/patient|caregiver/i.test(primaryActor)) return 'Patient handoff';
  if (/finance|payer|claims/i.test(primaryActor)) return 'Financial handoff';
  return 'Operational handoff';
}

const coverageMeaning = {
  IMPLEMENTED: 'Evidence exists; production certification is not implied',
  PARTIAL: 'A required branch, guard, state, or integration is missing',
  DECIDED: 'Logic is resolved; implementation is not proven',
  OPEN: 'A product, clinical, legal, or architecture decision is required',
  DEFERRED: 'Outside the current slice; must not appear live',
  'DESIGN-GAP': 'The current design omits or contradicts required logic',
};

function renderCoverageMatrix(sections, casesByJourney) {
  const journeys = sections.flatMap((section) => section.journeys);
  const coverageCounts = new Map();
  const relationshipCounts = new Map();

  for (const journey of journeys) {
    coverageCounts.set(
      journey.coverage,
      (coverageCounts.get(journey.coverage) ?? 0) + 1,
    );
    const relationship = doctorRelationship(journey);
    relationshipCounts.set(
      relationship,
      (relationshipCounts.get(relationship) ?? 0) + 1,
    );
  }

  const lines = [
    '<!-- Generated by scripts/generate-doctor-journey-coverage.mjs. Do not edit by hand. -->',
    '',
    '# Doctor journey coverage matrix',
    '',
    'This is the complete doctor-map projection of the canonical journey catalog. It includes direct doctor work and every patient, operational, system, financial, and administrative dependency that can change doctor-visible truth or closure.',
    '',
    'The relationship column describes placement in this map. It does not grant permission or establish ownership. Coverage is copied from the canonical catalog; the case count is derived from the canonical case matrix.',
    '',
    'Sources: [journey catalog](journey-catalog.md), [journey case matrix](journey-case-matrix.md), [doctor care-loop map](doctor-care-loop-map.md).',
    '',
    'Authority: the [source register](../05-traceability/source-register.md) governs conflicts. Current Kura platform backend evidence wins; legacy repositories are case and gap evidence only.',
    '',
    '## Coverage summary',
    '',
    `Total journeys: **${journeys.length}**.`,
    '',
    '| Coverage | Journeys | Meaning |',
    '| --- | ---: | --- |',
  ];

  for (const coverage of [
    'IMPLEMENTED',
    'PARTIAL',
    'DECIDED',
    'OPEN',
    'DEFERRED',
    'DESIGN-GAP',
  ]) {
    lines.push(
      `| ${coverage} | ${coverageCounts.get(coverage) ?? 0} | ${coverageMeaning[coverage]} |`,
    );
  }

  lines.push(
    '',
    '## Doctor-map relationship summary',
    '',
    '| Relationship | Journeys |',
    '| --- | ---: |',
  );

  for (const relationship of [
    'Direct doctor action',
    'Doctor continuity',
    'Administration boundary',
    'Authorization boundary',
    'System dependency',
    'Patient handoff',
    'Financial handoff',
    'Operational handoff',
  ]) {
    lines.push(
      `| ${relationship} | ${relationshipCounts.get(relationship) ?? 0} |`,
    );
  }

  for (const section of sections) {
    lines.push(
      '',
      `## ${section.title}`,
      '',
      '| ID | Journey | Primary actor | Doctor-map relationship | Entry → terminal outcome | Coverage | Cases |',
      '| --- | --- | --- | --- | --- | --- | ---: |',
    );

    for (const journey of section.journeys) {
      const caseCount = casesByJourney.get(journey.id)?.length ?? 0;
      lines.push(
        `| ${journey.id} | ${journey.journey} | ${journey.primaryActor} | ${doctorRelationship(journey)} | ${journey.outcome} | ${journey.coverage} | ${caseCount} |`,
      );
    }
  }

  lines.push(
    '',
    '## Completion rule',
    '',
    'A journey remains incomplete when a high-risk action has no reject and recovery case, a retry can duplicate a domain object or money event, an OPEN decision is represented as settled behavior, or a handoff has no next owner and persistent status.',
    '',
  );

  return lines.join('\n');
}

function validateInputs(sections, casesByJourney) {
  const journeys = sections.flatMap((section) => section.journeys);
  const journeyIds = new Set();

  for (const journey of journeys) {
    if (journeyIds.has(journey.id)) {
      throw new Error(`Duplicate journey ID in catalog: ${journey.id}`);
    }
    journeyIds.add(journey.id);

    if (!coverageMeaning[journey.coverage]) {
      throw new Error(
        `Unknown coverage ${journey.coverage} on journey ${journey.id}.`,
      );
    }

    if (!casesByJourney.has(journey.id)) {
      throw new Error(`Journey ${journey.id} has no case-matrix row.`);
    }
  }

  for (const journeyId of casesByJourney.keys()) {
    if (!journeyIds.has(journeyId)) {
      throw new Error(`Case matrix references unknown journey ${journeyId}.`);
    }
  }

  if (!fs.existsSync(careLoopPath)) return;

  const careLoop = fs.readFileSync(careLoopPath, 'utf8');
  const referencedIds = new Set(careLoop.match(/\b[A-Z]+-\d{2}\b/g) ?? []);
  const unknownIds = [...referencedIds].filter((id) => !journeyIds.has(id));

  if (unknownIds.length > 0) {
    throw new Error(
      `Doctor care-loop map references unknown journey IDs: ${unknownIds.join(', ')}`,
    );
  }
}

const catalog = fs.readFileSync(catalogPath, 'utf8');
const caseMatrix = fs.readFileSync(caseMatrixPath, 'utf8');
const sections = parseCatalog(catalog);
const casesByJourney = parseCaseCoverage(caseMatrix);

validateInputs(sections, casesByJourney);

const expected = renderCoverageMatrix(sections, casesByJourney);
const checkOnly = process.argv.includes('--check');

if (checkOnly) {
  const actual = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, 'utf8')
    : '';

  if (actual !== expected) {
    console.error(
      'Doctor journey coverage matrix is stale. Run `npm run docs:doctor-journey`.',
    );
    process.exit(1);
  }

  console.log(
    `Doctor journey docs are valid: ${sections.flatMap((section) => section.journeys).length} journeys, ${[...casesByJourney.values()].flat().length} cases.`,
  );
} else {
  fs.writeFileSync(outputPath, expected);
  console.log(`Generated ${path.relative(workspaceRoot, outputPath)}.`);
}
