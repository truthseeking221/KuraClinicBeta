export type HugeiconsProStyle = (typeof HUGEICONS_PRO_STYLES)[number];
/** @deprecated Use HugeiconsProStyle. Kept for existing Kura icon consumers. */
export type HugeiconsStyle = HugeiconsProStyle;

/** Chevron geometry stays crisp across compact disclosure and navigation controls. */
export const KURA_CHEVRON_ICON_STYLE: HugeiconsProStyle = "stroke-standard";

type KuraIconVariants = Record<HugeiconsProStyle, IconSvgElement>;

export type KuraIconProps = Omit<
  ComponentPropsWithoutRef<typeof HugeiconsIcon>,
  "altIcon" | "icon" | "showAlt"
> & {
  /** Selects one of the ten licensed Hugeicons Pro visual styles. */
  iconStyle?: HugeiconsProStyle;
};

export type KuraIconComponent = ForwardRefExoticComponent<
  KuraIconProps & RefAttributes<SVGSVGElement>
>;

function createKuraIcon(
  name: string,
  variants: KuraIconVariants,
  defaultStyle: HugeiconsProStyle = "stroke-rounded",
): KuraIconComponent {
  const Icon = forwardRef<SVGSVGElement, KuraIconProps>(function KuraIcon(
    {
      "aria-hidden": ariaHidden = true,
      "aria-label": ariaLabel,
      iconStyle = defaultStyle,
      size = "1em",
      ...props
    },
    ref,
  ) {
    return (
      <HugeiconsIcon
        ref={ref}
        icon={variants[iconStyle]}
        size={size}
        aria-hidden={ariaLabel ? undefined : ariaHidden}
        aria-label={ariaLabel}
        data-kura-icon={name}
        data-kura-icon-style={iconStyle}
        focusable="false"
        {...props}
      />
    );
  });

  Icon.displayName = `${name}Icon`;
  return Icon;
}

